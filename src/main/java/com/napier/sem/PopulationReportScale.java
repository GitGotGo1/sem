package com.napier.sem;

/**
 * Scale of a population report - whether it shows population of continent, region or country
 */
public enum PopulationReportScale {
    /**
     * Continent population report scale.
     */
    Continent,
    /**
     * Region population report scale.
     */
    Region,
    /**
     * Country population report scale.
     */
    Country
}